const os = require('os');

// Platform
// This should console.log the platform that you are using.
console.log(os.platform());

// CPU Architecture
// This should console.log the platform that you are using.
console.log(os.arch());

// CPU Core Info
console.log(os.cpus());

// Free Memory
console.log(os.freemem());

// Total Memory
console.log(os.totalmem());

// Home dir
console.log(os.homedir());

// UpTime 
console.log(os.uptime());